package java_basics.core.fundamentals;
class RelationalOperatorExample 
{
	public static void main(String[] args) 
	{
		// == , != , > , < , >= , <=
		int num1 = 10;
		int num2 = 50;
		System.out.println("Num1 == Num2 :"+(num1 == num2));
		System.out.println("Num1 != Num2 :"+(num1 != num2));
		System.out.println("Num1 > Num2 :"+(num1 > num2));
		System.out.println("Num1 < Num2 :"+(num1 < num2));
		System.out.println("Num1 < 10 :"+(num1 <= 10));
	}
}
